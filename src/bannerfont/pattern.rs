use num_enum::TryFromPrimitive;
use strum_macros::{EnumCount, EnumIter};

use super::Error;

#[repr(u8)]
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, TryFromPrimitive, EnumCount, EnumIter)]
pub enum Pattern {
    Base = 0,
    Border,
    Bricks,
    Circle,
    Creeper,
    Cross,
    CurlyBorder,
    DiagonalLeft,
    DiagonalUpRight,
    DiagonalUpLeft,
    DiagonalRight,
    Flower,
    Globe,
    Gradient,
    GradientUp,
    HalfHorizontal,
    HalfHorizontalBottom,
    HalfVertical,
    HalfVerticalRight,
    Mojang,
    Piglin,
    Rhombus,
    Skull,
    SmallStripes,
    SquareBottomLeft,
    SquareBottomRight,
    SquareTopLeft,
    SquareTopRight,
    StraightCross,
    StripeBottom,
    StripeCenter,
    StripeDownLeft,
    StripeDownRight,
    StripeLeft,
    StripeMiddle,
    StripeRight,
    StripeTop,
    TriangleBottom,
    TriangleTop,
    TrianglesBottom,
    TrianglesTop,
    Flow,
    Guster,
}

impl Pattern {
    pub fn code(self) -> &'static str {
        use Pattern::*;
        match self {
            Base => "b",
            Border => "bo",
            Bricks => "bri",
            Circle => "mc",
            Creeper => "cre",
            Cross => "cr",
            CurlyBorder => "cbo",
            DiagonalLeft => "ld",
            DiagonalUpRight => "rud",
            DiagonalUpLeft => "lud",
            DiagonalRight => "rd",
            Flower => "flo",
            Globe => "glb",
            Gradient => "gra",
            GradientUp => "gru",
            HalfHorizontal => "hh",
            HalfHorizontalBottom => "hhb",
            HalfVertical => "vh",
            HalfVerticalRight => "vhr",
            Mojang => "moj",
            Piglin => "pig",
            Rhombus => "mr",
            Skull => "sku",
            SmallStripes => "ss",
            SquareBottomLeft => "bl",
            SquareBottomRight => "br",
            SquareTopLeft => "tl",
            SquareTopRight => "tr",
            StraightCross => "sc",
            StripeBottom => "bs",
            StripeCenter => "cs",
            StripeDownLeft => "dls",
            StripeDownRight => "drs",
            StripeLeft => "ls",
            StripeMiddle => "ms",
            StripeRight => "rs",
            StripeTop => "ts",
            TriangleBottom => "bt",
            TriangleTop => "tt",
            TrianglesBottom => "bts",
            TrianglesTop => "tts",
            Flow => "flw",
            Guster => "gus",
        }
    }

    pub fn try_from_code(code: &str) -> Result<Self, Error> {
        use Pattern::*;
        Ok(match code {
            "b" => Base,
            "bo" => Border,
            "bri" => Bricks,
            "mc" => Circle,
            "cre" => Creeper,
            "cr" => Cross,
            "cbo" => CurlyBorder,
            "ld" => DiagonalLeft,
            "rud" => DiagonalUpRight,
            "lud" => DiagonalUpLeft,
            "rd" => DiagonalRight,
            "flo" => Flower,
            "glb" => Globe,
            "gra" => Gradient,
            "gru" => GradientUp,
            "hh" => HalfHorizontal,
            "hhb" => HalfHorizontalBottom,
            "vh" => HalfVertical,
            "vhr" => HalfVerticalRight,
            "moj" => Mojang,
            "pig" => Piglin,
            "mr" => Rhombus,
            "sku" => Skull,
            "ss" => SmallStripes,
            "bl" => SquareBottomLeft,
            "br" => SquareBottomRight,
            "tl" => SquareTopLeft,
            "tr" => SquareTopRight,
            "sc" => StraightCross,
            "bs" => StripeBottom,
            "cs" => StripeCenter,
            "dls" => StripeDownLeft,
            "drs" => StripeDownRight,
            "ls" => StripeLeft,
            "ms" => StripeMiddle,
            "rs" => StripeRight,
            "ts" => StripeTop,
            "bt" => TriangleBottom,
            "tt" => TriangleTop,
            "bts" => TrianglesBottom,
            "tts" => TrianglesTop,
            "flw" => Flow,
            "gus" => Guster,
            _ => return Err(Error::InvalidPattern(code.to_owned())),
        })
    }
}
